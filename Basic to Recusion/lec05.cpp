/*
 Generally we have time constraint of 10⁸ so keep check if using the complexity 
whether its worst case complexity rather not to exceed value of 10⁸

In Question we might see these given range for n ...

- n > 10⁸ → O(log n), O(1)
- n ≤ 10⁸ → O(n)
- n ≤ 10⁶ → O(n log n) → sorting
- n ≤ 10⁴ → O(n²)
- n ≤ 500 → O(n³)
- n ≤ 25 → O(2ⁿ) → Recursion, Brute Force
- n ≤ 12 → O(n!) → Recursion, Brute Force

*/